// Problem Description
// Create a program to print 3 numbers in a single statement.

// Print the numbers 5.55, 6.66, and 7.77 using a single cout statement.
// Separate each number using a comma followed by an empty space.
// Hint: Use the string ", " to print a comma followed by an empty space.

// Example
// Expected Output

// 5.55, 6.66, 7.77

#include<iostream>
using namespace std;

int main(){
    cout << 5.55 << "," << 6.66 << "," << 7.77;
    return 0;
}